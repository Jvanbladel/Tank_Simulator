package starter;

public class Cordinate {

	//Purpose hold x,y cord in an object
	
	private int x;
	
	private int y;
	
	//Constructor
	public Cordinate(int x, int y)
	{
		this.x = x;
		this.y =y;
	}
	
	//Get x cord
	public int getX()
	{
		return this.x;
	}
	
	//Get y cord
	public int getY()
	{
		return this.y;
	}
	
	//set x cord
	public void setX(int x)
	{
		this.x = x;
	}
	
	//set y cord
	public void setY(int y)
	{
		this.y = y;
	}
	
	
}
